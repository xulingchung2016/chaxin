package com.newbrain.model;

import java.util.List;

public class ShopsListBean    extends BaseJsonDataBean
{
	private List <shoplist> result;
	
	public List<shoplist> getResult() 
	{
		return result;
	}

	public void setResult(List<shoplist> result) 
	{
		this.result = result;
	}

	public class shoplist
	{
		public String getId()
		{
			return id;
		}
		
		public void setId(String id)
		{
			this.id = id;
		}
		
		public String getName()
		{
			return name;
		}
		
		public void setName(String name)
		{
			this.name = name;
		}
		
		public String getAddress()
		{
			return address;
		}
		
		public void setAddress(String address) 
		{
			this.address = address;
		}
		
		public String getTelphone()
		{
			return telphone;
		}
		
		public void setTelphone(String telphone)
		{
			this.telphone = telphone;
		}
		
		public String getImg() 
		{
			return img;
		}
		
		public void setImg(String img) 
		{
			this.img = img;
		}
		
		public String getLongitude() 
		{
			return longitude;
		}
		
		public void setLongitude(String longitude)
		{
			this.longitude = longitude;
		}
		
		public String getLatitude() 
		{
			return latitude;
		}
		
		public void setLatitude(String latitude)
		{
			this.latitude = latitude;
		}
		
		public String getGoods_score()
		{
			return goods_score;
		}
		
		public void setGoods_score(String goods_score) 
		{
			this.goods_score = goods_score;
		}
		
		public String getService_score() 
		{
			return service_score;
		}
		
		public void setService_score(String service_score) 
		{
			this.service_score = service_score;
		}
		
		public String getExpress_score() 
		{
			return express_score;
		}
		
		public void setExpress_score(String express_score) 
		{
			this.express_score = express_score;
		}
		
		private String id;
		private String name;
		private String address;
		private String telphone;
		private String img;
		private String longitude;
		private String latitude;
		private String goods_score;
		private String service_score;
		private String express_score;
	}
}
